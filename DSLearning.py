# List Data Structure in Python

my_list = [1, 2, 3, "four", "five"]

# to add an element at end of list

my_list.append(6)

# to remove an element from list

my_list.remove(2)

# to print full list

print(my_list)

# to print second element of list

print(my_list[2])

# to print fourth element of list

print(my_list[3])




# Dictionary Data Structure in Python

my_dict = {"name": "Zahid", "age": 20}

# to print a dict

print(my_dict["name"])

# to remove an element from dict

my_dict.pop("age")

# to add a value to dict

my_dict["age"] = 20





# Set Data Structure in Python

my_set = {1, 2, 3}

# to add a value to set

my_set.add(4)

# to remove a value from set

my_set.remove(4)





# Tuple Data Structure in Python

my_tuple = (1, 2)

print(my_tuple[0])